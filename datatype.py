# 資料: 程式的基本單位
# 數字
35456
3.513
# 字串
"測試中文"
"Hello world"
# 布林值
True
False
# 有順序、可動的列表 List
[3,4,5]
["Hello","world"]
# 有順序、不可變動的列表 Tuple
(3,4,5)
("Hello","world")
# 集合 Set
{3,4,5,6}
{"Hello","world"}
# 字典 Dictionary
{"apple":"蘋果","data":"資料"}
